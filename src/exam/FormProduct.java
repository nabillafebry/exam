    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            Statement statement = KoneksiDB.getKoneksi().createStatement();
            int productId = Integer.valueOf(txtProduct_ID.getText());
            String productName = txtProduct_Name.getText();
            int productStock = Integer.valueOf(txtProduct_Stock.getText());
        
            String query = "insert into purchasing.product values ("
                + productId + ","
                + "'" + productName + "',"
                + "'" + productStock + "')";
        
            statement.executeUpdate(query);
            
            getData();
            
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
            
            txtProduct_ID.setText("");
            txtProduct_Name.setText("");
            txtProduct_Stock.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(FormVendor.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

