    private void getData() {
        try {
            Statement statement = KoneksiDB.getKoneksi().createStatement();
            String sql = "SELECT * FROM product;";
            ResultSet rs = statement.executeQuery(sql);
            DefaultTableModel dtm = (DefaultTableModel) tblProduct.getModel();
            dtm.setRowCount(0);
            Object[] data = new Object[3];
            int i=1;
            
            while(rs.next()){
                data[0] = rs.getString("Product_ID");
                data[1] = rs.getString("Product_Name");
                data[2] = rs.getString("Stock");
                
                dtm.addRow(data);
                i++;
            }
        }
        catch (Exception e) {
            System.out.print(e);
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
