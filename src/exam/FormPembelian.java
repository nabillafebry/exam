    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            Statement statement = KoneksiDB.getKoneksi().createStatement();

            int purchaseId = Integer.valueOf(txtPurchase_ID.getText());
            int vendorId = Integer.valueOf(txtVendor_ID.getText());
            String vendorReffNo = txtVendor_ReffNo.getText();
            SimpleDateFormat Originalformat = new SimpleDateFormat("dd-MM-yyyy");
            SimpleDateFormat finalFormat = new SimpleDateFormat("yyyy-MM-dd");
            String purchaseDateStr = txtPurchasing_Date.getText();
            Date date = Originalformat.parse(purchaseDateStr);
            String purchaseDate = finalFormat.format(date);

            //Insert Table Purchase
            String queryPurchase = "insert into purchase values ("
                    + purchaseId + ","
                    + "'" + purchaseDate + "',"
                    + vendorId + ","
                    + "'" + vendorReffNo + "')";
            statement.executeUpdate(queryPurchase);

            String queryPurchaseDetail = "insert into purchase_detail values";
            List<Map<String, Object>> listProduct = new ArrayList();

            String infoStock = "Informasi Stock \n\n";

            if(!txtProduct_ID1.getText().equals("")){
                //select before
                ResultSet selectBefore = statement.executeQuery(
                        "select Stock from product "
                                + "where Product_ID="+txtProduct_ID1.getText());

                while (selectBefore.next()){
                    infoStock = infoStock.concat("Product ID : "
                            +txtProduct_ID1.getText()
                            +"\nStock Sebelum : "+ selectBefore.getString("Stock")
                            +"\n");
                }

                String queryProduct = "update product "
                        + "set Stock = `Stock`+"+txtQty1.getText()
                        + " where Product_ID="+txtProduct_ID1.getText()
                        +";";
                statement.executeUpdate(queryProduct);

                //select after
                ResultSet selectAfter = statement.executeQuery(
                        "select Stock from product "
                                + "where Product_ID="+txtProduct_ID1.getText());

                while (selectAfter.next()){
                    infoStock = infoStock.concat("Stock Sekarang : "
                            + selectAfter.getString("Stock")
                            +"\n\n");
                }

                Map<String, Object> product = new HashMap();
                product.put("productID", txtProduct_ID1.getText());
                product.put("qty", txtQty1.getText());
                product.put("price", txtPrice1.getText());
                product.put("subtotal", txtSubTotal1.getText());
                listProduct.add(product);
            }
            if(!txtProduct_ID2.getText().equals("")){
                //select before
                ResultSet selectBefore = statement.executeQuery(
                        "select Stock from product "
                                + "where Product_ID="+txtProduct_ID2.getText());

                while (selectBefore.next()){
                    infoStock = infoStock.concat("Product ID : "
                            +txtProduct_ID2.getText()
                            +"\nStock Sebelum : "+ selectBefore.getString("Stock")
                            +"\n");
                }

                String queryProduct = "update product "
                        + "set Stock = `Stock`+"+txtQty2.getText()
                        + " where Product_ID="+txtProduct_ID2.getText()
                        +";";
                statement.executeUpdate(queryProduct);

                //select after
                ResultSet selectAfter = statement.executeQuery(
                        "select Stock from product "
                                + "where Product_ID="+txtProduct_ID2.getText());

                while (selectAfter.next()){
                    infoStock = infoStock.concat("Stock Sekarang : "
                            + selectAfter.getString("Stock")
                            +"\n\n");
                }

                Map<String, Object> product = new HashMap();
                product.put("productID", txtProduct_ID2.getText());
                product.put("qty", txtQty2.getText());
                product.put("price", txtPrice2.getText());
                product.put("subtotal", txtSubTotal2.getText());
                listProduct.add(product);
            }
            if(!txtProduct_ID3.getText().equals("")){
                //select before
                ResultSet selectBefore = statement.executeQuery(
                        "select Stock from product "
                                + "where Product_ID="+txtProduct_ID3.getText());

                while (selectBefore.next()){
                    infoStock = infoStock.concat("Product ID : "
                            +txtProduct_ID3.getText()
                            +"\nStock Sebelum : "+ selectBefore.getString("Stock")
                            +"\n\n");
                }

                String queryProduct = "update product "
                        + "set Stock = `Stock`+"+txtQty3.getText()
                        + " where Product_ID="+txtProduct_ID3.getText()
                        +";";
                statement.executeUpdate(queryProduct);

                //select after
                ResultSet selectAfter = statement.executeQuery(
                        "select Stock from product "
                                + "where Product_ID="+txtProduct_ID3.getText());

                while (selectAfter.next()){
                    infoStock = infoStock.concat("Stock Sekarang : "
                            + selectAfter.getString("Stock")
                            +"\n");
                }

                Map<String, Object> product = new HashMap();
                product.put("productID", txtProduct_ID3.getText());
                product.put("qty", txtQty3.getText());
                product.put("price", txtPrice3.getText());
                product.put("subtotal", txtSubTotal3.getText());
                listProduct.add(product);
            }

            int grandTotal = 0;
            for (Map<String, Object> product : listProduct){
                queryPurchaseDetail = queryPurchaseDetail.concat(
                        "("+purchaseId+","
                                + product.get("productID")+","
                                + product.get("qty")+","
                                + product.get("price")+")"
                                + ",");

                String subtotal = (String) product.get("subtotal");
                grandTotal += Integer.valueOf(subtotal);
            }
            txtGrandTotal.setText(String.valueOf(grandTotal));
            queryPurchaseDetail = queryPurchaseDetail.substring(0, queryPurchaseDetail.length() - 1);
            statement.executeUpdate(queryPurchaseDetail);

            JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
            JOptionPane.showMessageDialog(null, infoStock);

        } catch (SQLException ex) {
            Logger.getLogger(FormPembelian.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(FormPembelian.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

