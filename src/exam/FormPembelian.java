    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        try {
            // TODO add your handling code here:
            Statement statement = KoneksiDB.getKoneksi().createStatement();
            String vendorID = txtVendor_ID.getText();
            String query = "select Vendor_Name from vendor "
                    + "where Vendor_ID like '%"+vendorID+"%'";
            
            ResultSet rs = statement.executeQuery(query);
            if (rs.next() == false) { 
                txtViewVendorName.setText("");
                JOptionPane.showMessageDialog(null, "Vendor belum terdaftar!");
            } else { 
                do { 
                    txtViewVendorName.setText(rs.getString("Vendor_Name"));
                } while (rs.next()); 
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(FormPembelian.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnViewActionPerformed

